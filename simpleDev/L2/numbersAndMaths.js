// order of operations 
// () comes first 
// * / same priority 
// + - same priority 
// we can use brackets to control which part gets done first 
// brackets are prioritzed first 

// 1 + 1 * 3 => 4 
// (1 + 1) * 3  => 6

// to calculate 10% - multiply number by 0.1 

Math.round(2.8) // this will round 2.8 upwards 

// calculating money in cents 
// $20.95 => 2095 cents 
// $7.99 => 799 cents 

Math.round((2095 + 799) * 0.1) / 100